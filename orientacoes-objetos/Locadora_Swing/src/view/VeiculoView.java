    private void btExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btExcluirActionPerformed
       int confirma = JOptionPane.showConfirmDialog(null, "Deseja realmente excluir este veiculo?","Exclusão de veiculo", JOptionPane.YES_NO_OPTION);
        
        System.out.println("confirma = " + confirma);
        
        if(confirma == 0){
            daoVeiculo.excluir(Integer.parseInt(tfCodigo.getText()));
            this.limparCampos();
            this.atualizarTabela();
        }
    }//GEN-LAST:event_btExcluirActionPerformed

