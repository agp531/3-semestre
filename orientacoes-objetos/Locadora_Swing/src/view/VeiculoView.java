    private void jSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jSalvarActionPerformed
        if(tfMarca.getText().equals("") ||tfModelo.getText().equals("") || tfChassi.getText().equals("") || tfAno.getText().equals("") ){
            JOptionPane.showMessageDialog(null, "Preenche tudo ai comédia.", "Aviso", JOptionPane.WARNING_MESSAGE);
        }
        else{
            veiculo = new Veiculo();
            veiculo.setMarca(tfMarca.getText());
            veiculo.setModelo(tfModelo.getText());
            veiculo.setChassi(tfChassi.getText());
            veiculo.setAno(Integer.parseInt(tfAno.getText()));
            if(tfCodigo.getText().equals("")){
                daoVeiculo.inserir(veiculo);
            }else{
                veiculo.setCodigo(Integer.parseInt(tfCodigo.getText()));
                daoVeiculo.alterar(veiculo);
            }
            this.atualizarTabela();
            this.limparCampos();
        }
    }//GEN-LAST:event_jSalvarActionPerformed

    private void tfAnoFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_tfAnoFocusLost
        try{
            int i = Integer.parseInt(tfAno.getText());
        }catch(NumberFormatException e){
            tfAno.setText("");
            JOptionPane.showMessageDialog(null, "O ano deve ser um numero inteiro comédia.", "Aviso", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_tfAnoFocusLost

    private void jtVeiculosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtVeiculosMouseClicked
        tfCodigo.setText(
                jtVeiculos.getValueAt(jtVeiculos.getSelectedRow(),VeiculoTableModel.COL_CODIGO).toString()
        );
        
        tfMarca.setText(
                jtVeiculos.getValueAt(jtVeiculos.getSelectedRow(),VeiculoTableModel.COL_MARCA).toString()
        );
        
        tfModelo.setText(
                jtVeiculos.getValueAt(jtVeiculos.getSelectedRow(),VeiculoTableModel.COL_MODELO).toString()
        );
        
        tfChassi.setText(
                jtVeiculos.getValueAt(jtVeiculos.getSelectedRow(),VeiculoTableModel.COL_CHASSI).toString()
        );
        
        tfAno.setText(
                jtVeiculos.getValueAt(jtVeiculos.getSelectedRow(),VeiculoTableModel.COL_ANO).toString()
        );
        btExcluir.setEnabled(true);
        
    }//GEN-LAST:event_jtVeiculosMouseClicked

